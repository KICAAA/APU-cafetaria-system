private void  LoaDataToTable(){ 
try {
            ArrayList<String> dataList = null;
            ManagerDaoImpl MDi = new ManagerDaoImpl();
            dataList = MDi.getAllOrder();
            
            DefaultTableModel dtm = new DefaultTableModel(new String[]{"Food ID", "Food Name", "Food Quantity", "Food Price"}, 0);
            Object[] tableLines = dataList.toArray();
            for (int i = 0; i < tableLines.length; i++) {
                String lines = tableLines[i].toString();
                String[] row = lines.split(",");
                dtm.addRow(row);

            }

            ordertable.setModel(dtm);

        } catch (Exception ex) {
        }
}
